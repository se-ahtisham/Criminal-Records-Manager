#include"Date.h"
Date::Date()
{
	day = 0;
	month = 0;
	year = 0;
}

void Date::setDay(int day) {
	day = day;
}
int Date::getDay() {
	return day;
}

void Date::setYear(int year) {
	year = year;
}
int Date::getYear() {
	return year;
}

void Date::setMonth(int month) {
	month = month;
}
int Date::getMonth() {
	return month;
}



